    private void load_table() {
                
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Tanggal");
        model.addColumn("Nama");
        model.addColumn("Jumlah");
        model.addColumn("Jenis");
        model.addColumn("Harga");
        model.addColumn("Ket");

        
        try {
            String sql = "select * from brg_msk";
            java.sql.Connection conn = (Connection) koneksi.koneksiDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            while (res.next()) {
                model.addRow(new Object[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), res.getString(5),res.getString(6),res.getString(7)});
            }
            tabel.setModel(model);
        } catch (SQLException e) {
        }
    }
    
